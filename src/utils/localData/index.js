const binary_search = (target) => {
  let left = 0;
  let right = dogs.length - 1;

  while (left < right) {
    let mid = Math.floor((left + right) / 2);
    if (dogs[mid] < target) {
      left = mid + 1;
    } else {
      right = mid;
    }
  }
  return dogs[(left + right) / 2] == target;
};

dogs.sort();

export default binary_search;
